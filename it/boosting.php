	<div class="title">ブースティングの概要</div>
	ブースティングとは学習アルゴリズムの１つ。<br>
	ブースティングは直接一つの精度の良い分類機を作るのではなく,
	精度の悪い分類機 (弱い分類機) を多数組み合わせることによって一つの精度の良い、
	強い分類機を作成する方法である。<br>
	弱い分類機は古典的なアルゴリズムが適しているようである。<br>
	強いアルゴリズムは弱い分類機の評価等をするようである。<br>
